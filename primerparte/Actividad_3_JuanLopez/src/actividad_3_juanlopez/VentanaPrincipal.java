    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // Codigo del Boton btnConsultar:
        double hora, hB, hN;
        hora = Double.parseDouble(txtHoras.getText());
        hB = SalarioNeto_SalarioBruto.Calcular_SalBruto(hora);
        hN = SalarioNeto_SalarioBruto.Calcular_SalNeto(hora);
        txtSalBruto.setText(String.valueOf(hB));
        txtSalNeto.setText(String.valueOf(hN));
    }//GEN-LAST:event_btnConsultarActionPerformed

