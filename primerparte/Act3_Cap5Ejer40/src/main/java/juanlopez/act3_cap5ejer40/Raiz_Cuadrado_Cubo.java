
package juanlopez.act3_cap5ejer40;

public class Raiz_Cuadrado_Cubo {
    public static double calcular_Raiz(double n) {
        double Raiz;
        Raiz = Math.sqrt(n);
        return Raiz;
    }

    public static double calcular_Cuadrado(double n) {
        double Cuadrado;
        Cuadrado = Math.pow(n, 2);
        return Cuadrado;
    }

    public static double calcular_Cubo(double n) {
        double Cubo;
        Cubo = Math.pow(n, 3);
        return Cubo;
    }
}
