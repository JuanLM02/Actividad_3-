    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // Codigo de btnCalcular
        double alt, bas, r_a, r_p;
        String r_t;
        alt = Double.parseDouble(txtAltura.getText());
        bas = Double.parseDouble(txtBase.getText());
        r_t = TrianguloRectangulo.determinarTipoTriangulo(bas,alt);
        r_a = TrianguloRectangulo.calcularArea(bas,alt);
        r_p = TrianguloRectangulo.calcularPerimetro(bas,alt);

        txtArea.setText(String.valueOf(r_a + "cm"));
        txtPerimetro.setText(String.valueOf(r_p + "cm"));
        txtTipo.setText(String.valueOf(r_t));
    }//GEN-LAST:event_btnCalcularActionPerformed

